def train_enn(top_k: int = CFG.ENN_TOP_K) -> EvidentialNetwork:
    """
    Executes the full Evidential Neural Network training pipeline.
    """
    print(f"[ENN] Loading Tutor Data from: {CFG.TUTOR_DIR}")

    # 1. Load and prepare datasets
    obs_tr, act_tr = _load_tutor_split(Path(CFG.TRAIN_FILE))
    obs_val, act_val = _load_tutor_split(Path(CFG.VAL_FILE))
    obs_te, act_te = _load_tutor_split(Path(CFG.TEST_FILE))

    (act_tr, act_val, act_te, mask_tr, mask_val, mask_te, cls_mapping, n_cls) = _remap_topk(act_tr, act_val, act_te, top_k=top_k)

    obs_tr = obs_tr[mask_tr]
    if obs_val is not None: obs_val = obs_val[mask_val]
    if obs_te is not None: obs_te = obs_te[mask_te]

    # 2. Compute handling strategies for imbalance and scale features
    class_weights = compute_effective_weights(act_tr, num_classes=n_cls, beta=0.999)
    print(f"[ENN] Calculated weights. Min: {class_weights.min():.3f} | Max: {class_weights.max():.3f}")

    scaler = _fit_and_save_scaler(obs_tr)
    obs_tr_s = scaler.transform(obs_tr)
    obs_val_s = scaler.transform(obs_val) if obs_val is not None else None
    obs_te_s = scaler.transform(obs_te) if obs_te is not None else None

    # 3. Initialize DataLoaders
    train_loader = DataLoader(
        NoisyDataset(obs_tr_s, act_tr, noise_std=CFG.ENN_NOISE_STD),
        batch_size=CFG.ENN_BATCH_SIZE,
        shuffle=True,
        num_workers=0
    )

    val_loader = None
    if obs_val_s is not None:
        val_loader = DataLoader(
            TensorDataset(torch.tensor(obs_val_s, dtype=torch.float32), torch.tensor(act_val, dtype=torch.long)),
            batch_size=CFG.ENN_BATCH_SIZE,
            shuffle=False
        )

    test_loader = None
    if obs_te_s is not None:
        test_loader = DataLoader(
            TensorDataset(torch.tensor(obs_te_s, dtype=torch.float32), torch.tensor(act_te, dtype=torch.long)),
            batch_size=CFG.ENN_BATCH_SIZE,
            shuffle=False
        )

    # 4. Initialize Model, Optimizer, and Scheduler
    model = EvidentialNetwork(
        input_dim=ENN_PARAMS["input_dim"],
        num_classes=n_cls,
        hidden_dim=ENN_PARAMS["hidden_dim"],
        dropout=CFG.ENN_DROPOUT
    ).to(DEVICE)

    optimizer = torch.optim.AdamW(model.parameters(), lr=CFG.ENN_MAX_LR, weight_decay=CFG.ENN_WEIGHT_DECAY)
    scheduler = _cosine_with_warmup(optimizer, CFG.ENN_WARMUP, CFG.ENN_EPOCHS)

    best_val_loss = float("inf")
    patience_counter = 0
    best_path = _best_weights_path()

    print("\n" + "=" * 80)
    print("  ENN TRAINING — Tutor CurriculumAgent (EDL Loss)")
    print("=" * 80)

    t0 = time.time()

    # 5. Execute Training Loop
    for epoch in range(1, CFG.ENN_EPOCHS + 1):
        model.train()
        tr_loss, tr_correct, tr_n = 0.0, 0, 0

        for obs_b, act_b in train_loader:
            obs_b, act_b = obs_b.to(DEVICE), act_b.to(DEVICE)

            out = model(obs_b)

            # Evidential loss computes NLL Type II + KL Divergence Penalty
            loss = evidential_loss(
                out["alpha"], act_b, epoch, CFG.ENN_EPOCHS,
                class_weights=class_weights,
                lam=CFG.ENN_EDL_LAMBDA
            )

            optimizer.zero_grad()
            loss.backward()

            # Gradient clipping prevents exploding gradients during the KL warmup phase
            nn.utils.clip_grad_norm_(model.parameters(), 1.0)
            optimizer.step()

            bs = obs_b.size(0)
            tr_loss += loss.item() * bs
            tr_correct += (out["prob"].argmax(-1) == act_b).sum().item()
            tr_n += bs

        scheduler.step()

        # 6. Evaluation Loop
        eval_loader = val_loader if val_loader is not None else test_loader
        val_info = {}

        if eval_loader is not None:
            model.eval()
            v_loss, v_correct, v_n = 0.0, 0, 0
            v_u = []

            with torch.no_grad():
                for obs_b, act_b in eval_loader:
                    obs_b, act_b = obs_b.to(DEVICE), act_b.to(DEVICE)
                    out = model(obs_b)

                    loss = evidential_loss(
                        out["alpha"], act_b, epoch, CFG.ENN_EPOCHS,
                        class_weights=class_weights,
                        lam=CFG.ENN_EDL_LAMBDA
                    )

                    bs = obs_b.size(0)
                    v_loss += loss.item() * bs
                    v_correct += (out["prob"].argmax(-1) == act_b).sum().item()
                    v_n += bs

                    # Track uncertainty to ensure it's behaving reasonably
                    v_u.append(out["uncertainty"].squeeze(-1).cpu())

            u_arr = torch.cat(v_u)
            val_info = {
                "val_loss": v_loss / v_n,
                "val_acc": v_correct / v_n,
                "u_mean": float(u_arr.mean()),
                "u_std": float(u_arr.std()),
            }

            # Standard Early Stopping implementation
            if val_info["val_loss"] < best_val_loss - 1e-5:
                best_val_loss = val_info["val_loss"]
                patience_counter = 0
                os.makedirs(os.path.dirname(best_path), exist_ok=True)
                torch.save(model.state_dict(), best_path)
            else:
                patience_counter += 1

        # 7. Logging
        if epoch <= 5 or epoch % 10 == 0:
            elapsed = time.time() - t0
            lr_now = optimizer.param_groups[0]["lr"]
            v_str = ""
            if val_info:
                v_str = (
                    f" | val_loss: {val_info['val_loss']:.4f} "
                    f"| val_acc: {val_info['val_acc']:.3f} "
                    f"| U: {val_info['u_mean']:.3f}±{val_info['u_std']:.3f}"
                )

            print(f"Epoch {epoch:3d}/{CFG.ENN_EPOCHS} "
                  f"| tr_loss: {tr_loss / tr_n:.4f} "
                  f"| tr_acc: {tr_correct / tr_n:.3f}"
                  f"{v_str} | lr: {lr_now:.1e} | [{elapsed:.0f}s]")

        if patience_counter >= CFG.ENN_PATIENCE:
            print(f"\n[ENN] Early stopping triggered at epoch {epoch}.")
            break

    # 8. Post-training: Load best weights and save artifacts
    if os.path.exists(best_path):
        model.load_state_dict(torch.load(best_path, map_location=DEVICE))

    os.makedirs(os.path.dirname(CFG.MODEL_ENN_PATH), exist_ok=True)
    torch.save(model.state_dict(), CFG.MODEL_ENN_PATH)

    with open(_meta_path(), "w") as f:
        json.dump({
            "num_classes": n_cls,
            "class_mapping": {str(k): v for k, v in cls_mapping.items()},
            "top_k": n_cls,
        }, f, indent=2)

    return model

if __name__ == "__main__":
    model = train_enn(top_k=CFG.ENN_TOP_K)
    print("\n[ENN] Pipeline Completed.")