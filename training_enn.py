def _load_scaler() -> StandardScaler:
    path = _scaler_path()
    if not os.path.exists(path):
        raise FileNotFoundError(f"Scaler not found at: {path}. Ensure the training pipeline ran successfully.")
    return joblib.load(path)

def _fit_and_save_scaler(obs: np.ndarray) -> StandardScaler:
    """Fits a scikit-learn StandardScaler to the training data and saves it to disk."""
    scaler = StandardScaler()
    scaler.fit(obs)
    path = _scaler_path()
    os.makedirs(os.path.dirname(path), exist_ok=True)
    joblib.dump(scaler, path)
    return scaler

def _load_enn_meta() -> dict:
    path = _meta_path()
    if os.path.exists(path):
        with open(path) as f:
            return json.load(f)
    return {"num_classes": CFG.ENN_NUM_CLASSES}

