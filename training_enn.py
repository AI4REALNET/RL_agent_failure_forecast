def _load_tutor_split(path: Path) -> Tuple[Optional[np.ndarray], Optional[np.ndarray]]:
    """Loads a specific npz split (train, val, or test) generated by the agent's tutor."""
    if not path.exists():
        return None, None

    data = np.load(str(path))
    files = list(data.files)

    # Heuristics to find state (s_) and action (a_) keys, regardless of naming conventions
    s_key = next((k for k in files if k.startswith("s_") or k == "obs"), None)
    a_key = next((k for k in files if k.startswith("a_") or k == "act"), None)

    obs = data[s_key].astype(np.float32)
    act = data[a_key].astype(np.int64).reshape(-1)
    return obs, act

def _remap_topk(act_tr: np.ndarray, act_val: Optional[np.ndarray], act_te: Optional[np.ndarray], top_k: int) -> Tuple:
    """
    Filters the dataset to only include the top-K most frequent actions.
    Power grids have massive discrete action spaces, but most actions are never used.
    Limiting to top-K drastically reduces output dimensionality and improves ENN stability.
    """
    counts = Counter(act_tr.tolist())
    topk = [cls for cls, _ in counts.most_common(top_k)]
    mapping = {old: new for new, old in enumerate(topk)}

    def apply(arr):
        if arr is None: return None, None
        mask = np.array([a in mapping for a in arr])
        return (np.array([mapping[a] for a in arr[mask]], dtype=np.int64), mask)

    act_tr_r, mask_tr = apply(act_tr)
    act_val_r, mask_val = apply(act_val)
    act_te_r, mask_te = apply(act_te)

    return (act_tr_r, act_val_r, act_te_r, mask_tr, mask_val, mask_te, mapping, top_k)

def compute_effective_weights(actions: np.ndarray, num_classes: int, beta: float = 0.999) -> torch.Tensor:
    """
    Computes Effective Number of Samples weights to handle extreme class imbalance.
    Prevents the model from being biased towards the "do nothing" action, ensuring
    rare topology-changing actions are penalized heavily if misclassified.
    Reference: https://arxiv.org/abs/1901.05555
    """
    counts = np.bincount(actions, minlength=num_classes)
    counts = np.maximum(counts, 1) # Avoid division by zero

    effective_num = 1.0 - np.power(beta, counts)
    weights = (1.0 - beta) / effective_num

    # Normalize weights so they sum to num_classes
    weights = weights / np.sum(weights) * num_classes

    return torch.tensor(weights, dtype=torch.float32, device=DEVICE)

class NoisyDataset(Dataset):
    """
    PyTorch Dataset wrapper that applies online Gaussian noise to observations.
    Acts as a data augmentation technique, improving the ENN's robustness to
    slight variations in grid states.
    """
    def __init__(self, obs, actions, noise_std):
        self.obs = torch.tensor(obs, dtype=torch.float32)
        self.actions = torch.tensor(actions, dtype=torch.long)
        self.noise_std = noise_std

    def __len__(self): return len(self.actions)

    def __getitem__(self, idx):
        x = self.obs[idx]
        if self.noise_std > 0:
            x = x + torch.randn_like(x) * self.noise_std
        return x, self.actions[idx]

def _cosine_with_warmup(optimizer, warmup, total):
    """
    Custom Learning Rate Scheduler:
    Linear warmup for early stability, followed by cosine annealing to smoothly
    converge to a minimum learning rate.
    """
    def lr_fn(ep):
        if ep < warmup: return ep / max(warmup, 1)
        p = (ep - warmup) / max(total - warmup, 1)
        return 0.5 * (1.0 + math.cos(math.pi * p))
    return torch.optim.lr_scheduler.LambdaLR(optimizer, lr_fn)

